package com.example.safedut;

public class User {
    public String name;
    public String studentNumber;
    public String email;
    public String role;
    public String campus;

    public User(String name, String studentNumber, String email, String role, String campus) {
        this.name = name;
        this.studentNumber = studentNumber;
        this.email = email;
        this.role = role;
        this.campus = campus;

    }


    public User() {}
}

